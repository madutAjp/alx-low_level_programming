#include <stdlib.h>
#include <stdio.h>

/**
 * madut_mod - To mod of two integer
 * @a:first integer
 * @b:second integer
 * Return:final answer
 */
int madut_mod(int a, int b)
{
        return (a % b);
}
/**
 * mul - To multiply two integer
 * @a:first integer
 * @b:second integer
 * Return:multiply
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * madut_sub - To subtract two integer
 * @a:first integer
 * @b:second integer
 * Return:subtracted final result
 */
int madut_sub(int a, int b)
{
        return (a - b);
}
/**
 * madut_add - To add two integer
 * @a:first integer
 * @b:second integer
 * Return:addition
 */
int madut_add(int a, int b)
{
	return (a + b);
}
